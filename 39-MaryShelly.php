<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Mary_Shelley">Wikipedia</a>
    <br>
    Mary Wollstonecraft Shelley (30 August 1797 – 1 February 1851) was an English novelist who wrote the Gothic novel Frankenstein; or, The Modern Prometheus (1818), which is considered an early example of science fiction.[2] She also edited and promoted the works of her husband, the Romantic poet and philosopher Percy Bysshe Shelley. Her father was the political philosopher William Godwin and her mother was the philosopher and feminist activist Mary Wollstonecraft.
    Shelley's mother died less than a month after giving birth to her. She was raised by her father, who provided her with a rich if informal education, encouraging her to adhere to his own anarchist political theories. When she was four, her father married a neighbour, Mary Jane Clairmont, with whom Shelley came to have a troubled relationship.[3][4]
    In 1814, Shelley began a romance with one of her father's political followers, Percy Bysshe Shelley, who was already married. Together with her stepsister, Claire Clairmont, she and Percy left for France and travelled through Europe. Upon their return to England, Shelley was pregnant with Percy's child. Over the next two years, she and Percy faced ostracism, constant debt and the death of their prematurely born daughter. They married in late 1816, after the suicide of Percy Shelley's first wife, Harriet.
    In 1816, the couple and Mary's stepsister famously spent a summer with Lord Byron and John William Polidori near Geneva, Switzerland, where Shelley conceived the idea for her novel Frankenstein. The Shelleys left Britain in 1818 for Italy, where their second and third children died before Shelley gave birth to her last and only surviving child, Percy Florence Shelley. In 1822, her husband drowned when his sailing boat sank during a storm near Viareggio. A year later, Shelley returned to England and from then on devoted herself to the upbringing of her son and a career as a professional author. The last decade of her life was dogged by illness, most likely caused by the brain tumour which killed her at age 53.
    Until the 1970s, Shelley was known mainly for her efforts to publish her husband's works and for her novel Frankenstein, which remains widely read and has inspired many theatrical and film adaptations. Recent scholarship has yielded a more comprehensive view of Shelley's achievements. Scholars have shown increasing interest in her literary output, particularly in her novels, which include the historical novels Valperga (1823) and Perkin Warbeck (1830), the apocalyptic novel The Last Man (1826) and her final two novels, Lodore (1835) and Falkner (1837). Studies of her lesser-known works, such as the travel book Rambles in Germany and Italy (1844) and the biographical articles for Dionysius Lardner's Cabinet Cyclopaedia (1829–1846), support the growing view that Shelley remained a political radical throughout her life. Shelley's works often argue that cooperation and sympathy, particularly as practised by women in the family, were the ways to reform civil society. 
    This view was a direct challenge to the individualistic Romantic ethos promoted by Percy Shelley and the Enlightenment political theories articulated by her father, William Godwin.
    After her husband's death, Mary Shelley lived for a year with Leigh Hunt and his family in Genoa, where she often saw Byron and transcribed his poems. She resolved to live by her pen and for her son, but her financial situation was precarious. On 23 July 1823, she left Genoa for England and stayed with her father and stepmother in the Strand until a small advance from her father-in-law enabled her to lodge nearby.[119] Sir Timothy Shelley had at first agreed to support his grandson, Percy Florence, only if he were handed over to an appointed guardian. Mary Shelley rejected this idea instantly.[120] She managed instead to wring out of Sir Timothy a limited annual allowance (which she had to repay when Percy Florence inherited the estate), but to the end of his days, he refused to meet her in person and dealt with her only through lawyers. Mary Shelley busied herself with editing her husband's poems, among other literary endeavours, but concern for her son restricted her options. Sir Timothy threatened to stop the allowance if any biography of the poet were published.[121] In 1826, Percy Florence became the legal heir of the Shelley estate after the death of his half-brother Charles Shelley, his father's son by Harriet Shelley. Sir Timothy raised Mary's allowance from £100 a year to £250 but remained as difficult as ever.[122] Mary Shelley enjoyed the stimulating society of William Godwin's circle, but poverty prevented her from socialising as she wished. She also felt ostracised by those who, like Sir Timothy, still disapproved of her relationship with Percy Bysshe Shelley.[123]
    In the summer of 1824, Mary Shelley moved to Kentish Town in north London to be near Jane Williams. She may have been, in the words of her biographer Muriel Spark, "a little in love" with Jane. Jane later disillusioned her by gossiping that Percy had preferred her to Mary, owing to Mary's inadequacy as a wife.[124] At around this time, Mary Shelley was working on her novel, The Last Man (1826); and she assisted a series of friends who were writing memoirs of Byron and Percy Shelley—the beginnings of her attempts to immortalise her husband.[125] She also met the American actor John Howard Payne and the American writer Washington Irving, who intrigued her. Payne fell in love with her and in 1826 asked her to marry him. She refused, saying that after being married to one genius, she could only marry another.
    Payne accepted the rejection and tried without success to talk his friend Irving into proposing himself. Mary Shelley was aware of Payne's plan, but how seriously she took it is unclear.
