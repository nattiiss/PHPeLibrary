<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Astrid_Lindgren">Wikipedia</a>
    <br>
    Astrid Anna Emilia Lindgren (14 November 1907 – 28 January 2002) was a Swedish writer of fiction and screenplays. She is best known for several children's book series, 
    featuring Pippi Longstocking, Emil of Lönneberga, Karlsson-on-the-Roof, and the Six Bullerby Children (Children of Noisy Village in the US), and for the children's fantasy novels 
    Mio, My Son, Ronia the Robber's Daughter, and The Brothers Lionheart. Lindgren worked on the Children's Literature Editorial Board at the Rabén & Sjögren publishing house in Stockholm 
    and wrote more than 30 books for children.[4] In January 2017, she was calculated to be the world's 18th most translated author, and the fourth most translated children's writer 
    after Enid Blyton, Hans Christian Andersen and the Brothers Grimm. Lindgren has so far sold roughly 167 million books worldwide. In 1994, she was awarded the Right Livelihood 
    Award for "her unique authorship dedicated to the rights of children and respect for their individuality."
    In 1976, a scandal arose in Sweden when it was publicised that Lindgren's marginal tax rate had risen to 102 percent. This was to be known as the "Pomperipossa effect", from a story she published in Expressen on 3 March 1976,[24] entitled Pomperipossa in Monismania, attacking the government and its taxation policies.[25] It was a satirical allegory in response to the marginal tax rate Lindgren had incurred in 1976,[26] which required self-employed individuals to pay both regular income tax and employers' deductions.[26] In a stormy tax debate, she attracted criticism from Social Democrats and even from her own colleagues, and responded by raising the issue of the lack of women involved in the Social Democrats' campaign.
    In that year's general election, the Social Democratic government was voted out for the first time in 44 years, and the Lindgren tax debate was one of several controversies that may have contributed to the result. Another controversy involved Ingmar Bergman's farewell letter to Sweden, after charges had been made against him of tax evasion.[25] Lindgren nevertheless remained a Social Democrat for the rest of her life.[28]
    In 1978, when she received the Peace Prize of the German Book Trade, Lindgren made a speech, Never Violence!. She spoke against corporal punishment of children. After that she teamed up with scientists, journalists and politicians to promote non-violent upbringing. In 1979, a law was introduced in Sweden prohibiting violence against children.[29] Until then there was no such law anywhere in the world.[30]
    From 1985 to 1989, Lindgren wrote articles concerning animal protection and mass production in the Swedish magazines Expressen and Dagens Nyheter along with the veterinarian Kristina Forslund. They wanted to launch an awareness campaign to promote better animal treatment in factory farming. Eventually their activities led to a new law which was called Lex Lindgren and was presented to Lindgren on her 80th birthday. During that time it was the strictest law concerning animal welfare in the world.[31] However, Lindgren and Forslund were unsatisfied with it. Not enough had been done and only minor changes occurred. The articles Forslund and Lindgren wrote were later published in the book Min ko vill ha roligt.
    Lindgren was well known both for her support for children's and animal rights and for her opposition to corporal punishment and the EU. In 1994 she received the Right Livelihood Award, "For her commitment to justice, non-violence and understanding of minorities as well as her love and caring for nature."
    Lindgren was also a member of the freedom of speech-promoting, anti-imperialist organization Folket i Bild/Kulturfront.
    In memory of Lindgren, a memorial sculpture was created next to her childhood home, named Källa Astrid ("Astrid's Wellspring" in English). 
    It is situated at the spot where Lindgren first heard fairy tales. The sculpture consists of an artistic representation of a young person's head (1.37 m high),
    flattened on top, in the corner of a square pond, and, just above the water, a ring of rosehip thorn (with a single rosehip bud attached to it). The sculpture was initially 
    slightly different in design and intended to be part of a fountain set in the city center, but the people of Vimmerby vehemently opposed the idea. Furthermore, Lindgren had stated 
    that she never wanted to be represented as a statue. (However, there is a statue of Lindgren in the city center.) The memorial was sponsored by the culture council of Vimmerby.

