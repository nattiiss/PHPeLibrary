<h2>Biography</h2>
    <p>
    <br>
    <a href=" https://en.wikipedia.org/wiki/Ernest_Hemingway">Wikipedia</a>
    <br>
    Ernest Miller Hemingway (July 21, 1899 – July 2, 1961) was an American novelist, short-story writer, and journalist. His economical and understated style—which he termed the 
    iceberg theory—had a strong influence on 20th-century fiction, while his adventurous lifestyle and public image brought him admiration from later generations. Hemingway produced 
    most of his work between the mid-1920s and the mid-1950s, and he was awarded the 1954 Nobel Prize in Literature. He published seven novels, six short-story collections, and two
    nonfiction works. Three of his novels, four short-story collections, and three nonfiction works were published posthumously. Many of his works are considered classics of American 
    literature. Hemingway was raised in Oak Park, Illinois. After high school, he was a reporter for a few months for The Kansas City Star before leaving for the Italian Front to enlist 
    as an ambulance driver in World War I. In 1918, he was seriously wounded and returned home. His wartime experiences formed the basis for his novel A Farewell to Arms (1929). In 1921, 
    he married Hadley Richardson, the first of four wives. They moved to Paris where he worked as a foreign correspondent and fell under the influence of the modernist writers and 
    artists of the 1920s' "Lost Generation" expatriate community. Hemingway's debut novel The Sun Also Rises was published in 1926. He divorced Richardson in 1927, and married Pauline
    Pfeiffer. They divorced after he returned from the Spanish Civil War (1936–1939), which he covered as a journalist and which was the basis for his novel For Whom the Bell Tolls (1940). 
    Martha Gellhorn became his third wife in 1940. He and 
    Gellhorn separated after he met Mary Welsh in London during World War II. Hemingway was present with Allied troops as a journalist at the Normandy landings and the liberation of Paris.
    He maintained permanent residences in Key West, Florida (in the 1930s) and in Cuba (in the 1940s and 1950s). He almost died in 1954 after two plane crashes on successive days, 
    with injuries leaving him in pain and ill health for much of the rest of his life. In 1959, he bought a house in Ketchum, Idaho, where, in mid-1961, he committed suicide.
    Hemingway's writing includes themes of love, war, travel, wilderness, and loss.[194] Hemingway often wrote about Americans abroad. "In six of the seven novels published during his lifetime," writes Jeffrey Herlihy in Hemingway's Expatriate Nationalism, "the protagonist is abroad, bilingual, and bicultural."[195] Herlihy calls this "Hemingway's Transnational Archetype" and argues that the foreign settings, 
    "far from being mere exotic backdrops or cosmopolitan milieus, are motivating factors in-character action."[195] Critic Leslie Fiedler sees the theme he defines as "The Sacred Land"—the American West—extended in Hemingway's work to include mountains in Spain, Switzerland and Africa, and to the streams of Michigan. The American West is given a symbolic nod with the naming of the "Hotel Montana" in The Sun 
    Also Rises and For Whom the Bell Tolls. According to Stoltzfus and Fiedler, in Hemingway's work, nature is a place for rebirth and rest; and it is where the hunter or fisherman might
    experience a moment of transcendence at the moment they kill their prey.[197] Nature is where men exist without women: men fish; men hunt; men find redemption in nature.
    Although Hemingway does write about sports, such as fishing, Carlos Baker notes the emphasis is more on the athlete than the sport.[198] At its core, much of Hemingway's work can 
    be viewed in the light of American naturalism, evident in detailed descriptions such as those in "Big Two-Hearted River".
Fiedler believes Hemingway inverts the American literary theme of the evil "Dark Woman" versus the good "Light Woman". The dark woman—Brett Ashley of The Sun Also Rises—is a goddess; 
the light woman—Margot Macomber of "The Short Happy Life of Francis Macomber"—is a murderess.[196] Robert Scholes says early Hemingway stories, such as "A Very Short Story", present "a 
male character favorably and a female unfavorably".[199] According to Rena Sanderson, early Hemingway critics lauded his male-centric world of masculine pursuits, and the fiction divided
 women into "castrators or love-slaves". Feminist critics attacked Hemingway as "public enemy number one", although more recent re-evaluations of his work "have given new visibility to 
 Hemingway's female characters (and their strengths) and have revealed his own sensitivity to gender issues, thus casting doubts on the old assumption that his writings were one-sidedly 
 masculine."[200] Nina Baym believes that Brett Ashley and Margot Macomber "are the two outstanding examples of Hemingway's 'bitch women.'"
