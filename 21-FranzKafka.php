<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Franz_Kafka">Wikipedia</a>
    <br>
    Franz Kafka[a] (3 July 1883 – 3 June 1924) was a German-speaking Bohemian novelist and short-story writer, widely regarded as one of the major figures of 20th-century literature. His work fuses elements of realism and the fantastic.[4] It typically features isolated protagonists facing bizarre or surrealistic predicaments and incomprehensible socio-bureaucratic powers. It has been interpreted as exploring themes of alienation, existential anxiety, guilt, and absurdity.
    His best known works include the short story "The Metamorphosis" and novels The Trial and The Castle. The term Kafkaesque has entered English to describe situations like those found in his writing.[6]
    Kafka was born into a middle-class German-speaking Czech Jewish family in Prague, the capital of the Kingdom of Bohemia, then part of the Austro-Hungarian Empire, today the capital 
    of the Czech Republic.[7] He trained as a lawyer and after completing his legal education was employed full-time by an insurance company, forcing him to relegate writing to his spare time. Over the course of his life, Kafka wrote hundreds of letters to family and close friends, including his father, 
    with whom he had a strained and formal relationship. He became engaged to several women but never married. He died in 1924 at the age of 40 from tuberculosis.
    Few of Kafka's works were published during his lifetime: the story collections Contemplation and A Country Doctor, and individual stories (such as "The Metamorphosis") were published 
    in literary magazines but received little public attention. In his will, Kafka instructed his executor and friend Max Brod to destroy his unfinished works, including his novels 
    The Trial, The Castle, and Amerika, but Brod ignored these instructions, and had the bulk of his work published. Kafka's work has influenced a vast range of writers, critics, artists, 
    and philosophers during the 20th and 21st centuries. Kafka's earliest published works were eight stories which appeared in 1908 in the first issue of the literary journal Hyperion under the title Betrachtung (Contemplation). He wrote the story "Beschreibung eines Kampfes" ("Description of a Struggle")[c] in 1904; he showed it to Brod in 1905 who advised him to continue writing and convinced him to submit it to Hyperion. Kafka published a fragment in 1908[142] and two sections in the spring of 1909, all in Munich.[143]
    In a creative outburst on the night of 22 September 1912, Kafka wrote the story "Das Urteil" ("The Judgment", literally: "The Verdict") and dedicated it to Felice Bauer. Brod noted the similarity in names of the main character and his fictional fiancée, Georg Bendemann and Frieda Brandenfeld, to Franz Kafka and Felice Bauer.[144] The story is often considered Kafka's breakthrough work. It deals with the troubled relationship of a son and his dominant father, facing a new situation after the son's engagement.[145][146] Kafka later described writing it as "a complete opening of body and soul",[147] a story that "evolved as a true birth, 
    covered with filth and slime".[148] The story was first published in Leipzig in 1912 and dedicated "to Miss Felice Bauer", and in subsequent editions "for F."
    In 1912, Kafka wrote "Die Verwandlung" ("The Metamorphosis", or "The Transformation"),[149] published in 1915 in Leipzig. The story begins with a travelling salesman waking to find 
    himself transformed into an ungeheures Ungeziefer, a monstrous vermin, Ungeziefer being a general term for unwanted and unclean pests, especially insects. Critics regard the work as 
    one of the seminal works of fiction of the 20th century.[150][151][152] The story "In der Strafkolonie" ("In the Penal Colony"), dealing with an elaborate torture and execution device, 
    was written in October 1914,[82] revised in 1918, and published in Leipzig during October 1919. The story "Ein Hungerkünstler" ("A Hunger Artist"), published in the periodical Die neue 
    Rundschau in 1924, describes a victimized protagonist who experiences a decline in the appreciation of his strange craft of starving himself for extended periods.[153] His last story, 
    "Josefine, die Sängerin oder Das Volk der Mäuse" 
    ("Josephine the Singer, or the Mouse Folk"), also deals with the relationship between an artist and his audience
