<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Milan_Kundera">Wikipedia</a>
    <br>
    Milan Kundera ((listen); born 1 April 1929) is a Czech writer who went into exile in France in 1975, becoming a naturalised French citizen in 1981. Kundera's Czechoslovak citizenship was revoked in 1979; he received his Czech citizenship in 2019. He "sees himself as a French writer and insists his work should be studied as French literature and classified as such in book stores".[5]
    Kundera's best-known work is The Unbearable Lightness of Being. Prior to the Velvet Revolution of 1989, the communist régime in Czechoslovakia banned his books. He leads a low-profile life and rarely speaks to the media.[6] He was thought to be a contender for the Nobel Prize in Literature, and was also a nominee for other awards.[7][8]
    He was awarded the 1985 Jerusalem Prize, in 1987 the Austrian State Prize for European Literature, and the 2000 Herder Prize. In 2021 he received the Golden Order of Merit from the president of Slovenia, Borut Pahor.
    Although his early poetic works are staunchly pro-communist,[16][17] his novels escape ideological classification. Kundera has repeatedly insisted that he is a novelist rather than a politically motivated writer. Political commentary has all but disappeared from his novels since the publication of The Unbearable Lightness of Being except in relation to broader philosophical themes. Kundera's style of fiction, interlaced with philosophical digression, is 
    greatly inspired by the novels of Robert Musil and the philosophy of Nietzsche,[18] and is also interpreted philosophically by authors such as Alain de Botton and Adam Thirlwell.
    Kundera himself claims inspiration from Renaissance authors such as Giovanni Boccaccio, Rabelais, and perhaps most importantly, Miguel de Cervantes, to whose legacy he considers 
    himself most committed. Other influences include Laurence Sterne, Henry Fielding, Denis Diderot, Robert Musil, Witold Gombrowicz, Hermann Broch, Franz Kafka, and Martin Heidegger.
    Originally he wrote in Czech, but from 1993 on he has written his novels in French. Between 1985 and 1987, he undertook the revision of the French translations of his earlier works 
    himself. His books have also been translated into many other languages. Kundera was born in 1929 at Purkyňova 6 (6 Purkyně Street) in Královo Pole, a quarter of Brno, Czechoslovakia, to a middle-class family. His father, Ludvík Kundera (1891–1971), was an important Czech musicologist and pianist who served as the head of the Janáček Music Academy in Brno from 1948 to 1961. His mother was Milada Kunderová (born Janošíková).
    Milan learned to play the piano from his father and later studied musicology and musical composition. Musicological influences, references and notation can be found throughout his work. Kundera is a cousin of Czech writer and translator Ludvík Kundera. He belonged to the generation of young Czechs who had had little or no experience of the pre-war democratic Czechoslovak Republic. Their ideology was greatly influenced by the experiences of World War II and the German occupation.
    Still in his teens, he joined the Communist Party of Czechoslovakia which seized power in 1948. He completed his secondary school studies in Brno at Gymnázium třída Kapitána Jaroše in 1948. He studied literature and aesthetics at the Faculty of Arts at Charles University in Prague. After two terms, he transferred to the Film Faculty of the Academy of Performing Arts in Prague where he first attended lectures in film direction and script writing.
    In 1950, his studies were interrupted when he and writer Jan Trefulka were expelled from the Communist Party for "anti-party activities." Trefulka described the incident in his novella Pršelo jim štěstí (Happiness Rained on Them, 1962). Kundera also used the expulsion as an inspiration for the main theme of his novel Žert (The Joke, 1967). After Kundera graduated in 1952, the Film Faculty appointed him a lecturer in world literature. In 1956 Kundera was readmitted to the Party but was expelled for the second time in 1970.[10][11]
    Along with other reformist communist writers such as Pavel Kohout, he was peripherally involved in the 1968 Prague Spring. This brief period of reformist activities was crushed by the Soviet invasion of Czechoslovakia in August 1968. Kundera remained committed to reforming Czechoslovak communism, and argued vehemently in print with fellow Czech writer Václav Havel, saying, essentially, that everyone should remain calm and that "nobody is being locked up for his opinions yet," and "the significance of the Prague Autumn may ultimately be greater than that of the Prague Spring." Finally, however, Kundera relinquished his reformist dreams and moved to France in 1975. He taught for a few years in the University of Rennes.[12][13] He was stripped of Czechoslovak citizenship in 1979; he has been a French citizen since 1981.[14]
    He maintains contact with Czech and Slovak friends in his homeland, but rarely returns and always does so without fanfare.
