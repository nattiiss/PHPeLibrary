    <br>
    Oscar Fingal O'Flahertie Wills Wilde (16 October 1854 â€“ 30 November 1900) was an Irish poet and playwright. After writing in different forms throughout the 1880s, he became one of the most popular playwrights in London in the early 1890s. He is best remembered for his epigrams and plays, his novel The Picture of Dorian Gray, and the circumstances of his criminal conviction for gross indecency for consensual homosexual acts in "one of the first celebrity trials",[1] imprisonment, and early death from meningitis at age 46.
    Wilde's parents were Anglo-Irish intellectuals in Dublin. A young Wilde learned to speak fluent French and German. At university, Wilde read Greats; he demonstrated himself to be an exceptional classicist, first at Trinity College Dublin, then at Oxford. He became associated with the emerging philosophy of aestheticism, led by two of his tutors, Walter Pater and John Ruskin. After university, Wilde moved to London into fashionable cultural and social circles.
    As a spokesman for aestheticism, he tried his hand at various literary activities: he published a book of poems, lectured in the United States and Canada on the new "English Renaissance in Art" and interior decoration, and then returned to London where he worked prolifically as a journalist. Known for his biting wit, flamboyant dress and glittering conversational skill, Wilde became one of the best-known personalities of his day. At the turn of the 1890s, he refined his ideas about the supremacy of art in a series of dialogues and essays, and 
    incorporated themes of decadence, duplicity, and beauty into what would be his only novel, The Picture of Dorian Gray (1890). The opportunity to construct aesthetic details precisely, and combine them with larger social themes, drew Wilde to write drama. He wrote Salome (1891) in French while in Paris, but it was refused a licence for England due to an absolute prohibition on the portrayal of Biblical subjects on the English stage. Unperturbed, Wilde produced 
    four society comedies in the early 1890s, which made him one of the most successful playwrights of late-Victorian London.
    At the height of his fame and success, while The Importance of Being Earnest (1895) was still being performed in London, Wilde prosecuted the Marquess of Queensberry for criminal libel.[2] The Marquess was the father of Wilde's lover, Lord Alfred Douglas. The libel trial unearthed evidence that caused Wilde to drop his charges and led to his own arrest and trial for gross indecency with men.[3] After two more trials he was convicted and sentenced to two years' hard labour, the maximum penalty, and was jailed from 1895 to 1897.[4] During his last year in prison, he wrote De Profundis (published posthumously in 1905), a long letter which discusses his spiritual journey through his trials, forming a dark counterpoint to his earlier philosophy of pleasure. On his release, he left immediately for France, and never returned to Ireland or Britain. There he wrote his last work, The Ballad of Reading Gaol (1898), a long poem commemorating the harsh rhythms of prison life.
    Wilde published The Happy Prince and Other Tales in 1888, and had been regularly writing fairy stories for magazines. In 1891 he published two more collections, Lord Arthur Savile's Crime and Other Stories, and in September A House of Pomegranates was dedicated "To Constance Mary Wilde".[96] "The Portrait of Mr. W. H.", which Wilde had begun in 1887, was first published in Blackwood's Edinburgh Magazine in July 1889.[97] It is a short story, which reports a conversation, in which the theory that Shakespeare's sonnets were written out of the poet's love of the boy actor "Willie Hughes", is advanced, retracted, and then propounded again. The only evidence for this is two supposed puns within the sonnets themselves.[98]
    The anonymous narrator is at first sceptical, then believing, finally flirtatious with the reader: he concludes that "there is really a great deal to be said of the Willie Hughes theory of Shakespeare's sonnets."[99] By the end fact and fiction have melded together.[100] Arthur Ransome wrote that Wilde "read something of himself into Shakespeare's sonnets" and became fascinated with the "Willie Hughes theory" despite the lack of biographical evidence for the historical William Hughes' existence.[101] Instead of writing a short but serious essay on the question, Wilde tossed the theory amongst the three characters of the story, allowing it to unfold as background to the plot. The story thus is an early masterpiece of Wilde's combining many elements that interested him: conversation, literature and the idea that to shed oneself of an idea one must first convince another of its truth.[102] Ransome concludes that Wilde succeeds precisely because the literary criticism is unveiled with such a deft touch.
    Though containing nothing but "special pleading", it would not, he says "be possible to build an airier castle in Spain than this of the imaginary William Hughes" we continue listening nonetheless to be charmed by the telling.[103] "You must believe in Willie Hughes," Wilde told an acquaintance, "I almost do, myself."[
