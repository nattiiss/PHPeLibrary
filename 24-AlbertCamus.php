<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Albert_Camus">Wikipedia</a>
    <br>
    Albert Camus (French: [albɛʁ kamy] (listen); 7 November 1913 – 4 January 1960) was an Algerian-born French philosopher, author, dramatist and journalist. He was awarded the 1957 Nobel 
    Prize in Literature at the age of 44, the second-youngest recipient in history. His works include The Stranger, The Plague, The Myth of Sisyphus, The Fall, and The Rebel.
    Camus was born in French Algeria to Pieds Noirs parents. He spent his childhood in a poor neighbourhood and later studied philosophy at the University of Algiers. He was in Paris when 
    the Germans invaded France during World War II in 1940. Camus tried to flee but finally joined the French Resistance where he served as editor-in-chief at Combat, an outlawed newspaper. 
    After the war, he was a celebrity figure and gave many lectures around the world. He married twice but had many extramarital affairs. Camus was politically active; he was part of the 
    left that opposed the Soviet Union because of its totalitarianism. Camus was a moralist and leaned towards anarcho-syndicalism. He was part of many organisations seeking European 
    integration. During the Algerian War (1954–1962), he kept a neutral stance, advocating for a multicultural and pluralistic Algeria, a position that caused controversy and was rejected 
    by most parties.
    Philosophically, Camus's views contributed to the rise of the philosophy known as absurdism. Some consider Camus's work to show him to be an existentialist, even though he 
    himself firmly rejected the term throughout his lifetime.
    In 1934, aged 20, Camus was in a relationship with Simone Hié.[11] Simone had an addiction to morphine, a drug she used to ease her menstrual pains. His uncle Gustave did not approve of the relationship, but Camus married Hié to help her fight her addiction. He subsequently discovered she was in a relationship with her doctor at the same time and the couple later divorced.[6]
    Camus joined the French Communist Party (PCF) in early 1935. He saw it as a way to "fight inequalities between Europeans and 'natives' in Algeria," even though he was not a Marxist. He explained: "We might see communism as a springboard and asceticism that prepares the ground for more spiritual activities." Camus left the PCF a year later.[12] In 1936, the independence-minded Algerian Communist Party (PCA) was founded, and Camus joined it after his mentor Grenier advised him to do so. Camus's main role within the PCA was to organise the Théâtre du Travail ("Workers' Theatre"). Camus was also close to the Parti du 
    Peuple Algérien (Algerian People's Party (PPA)), which was a moderate anti-colonialist/nationalist party. As tensions in the interwar period escalated, the Stalinist PCA and PPA broke ties. Camus was expelled from the PCA for refusing to toe the party line. This series of events sharpened his belief in human dignity. Camus's mistrust of bureaucracies that aimed for efficiency instead of justice grew. He continued his involvement with theatre and renamed his group Théâtre de l'Equipe ("Theatre of the Team"). Some of his scripts were the basis for his later novels.[13]
    In 1938, Camus began working for the leftist newspaper Alger républicain (founded by Pascal Pia) as he had strong anti-fascist feelings, and the rise of fascist regimes in Europe was worrying him. By then, Camus had developed strong feelings against authoritative colonialism as he witnessed the harsh treatment of the Arabs and Berbers by French authorities. Alger républicain was banned in 1940 and Camus flew to Paris to take a new job at Paris-Soir as editor-in-chief. In Paris, he almost completed his "first cycle" of works dealing with the absurd and the meaningless—the novel L'Étranger (The Outsider (UK), or The Stranger (US)), the philosophical essay Le Mythe de Sisyphe (The Myth of Sisyphus) and the play Caligula. Each cycle consisted of a novel, an essay and a theatrical play.
