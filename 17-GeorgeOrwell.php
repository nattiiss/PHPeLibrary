<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/George_Orwell">Wikipedia</a>
    <br>
    Eric Arthur Blair (25 June 1903 – 21 January 1950), known by his pen name George Orwell, was an English novelist, essayist, journalist and critic.[1] His work is characterised by lucid prose, social criticism, opposition to totalitarianism, and support of democratic socialism.[2]
    Orwell produced literary criticism and poetry, fiction and polemical journalism. He is known for the allegorical novella Animal Farm (1945) and the dystopian novel Nineteen Eighty-Four (1949). His non-fiction works, including The Road to Wigan Pier (1937), documenting his experience of working-class 
    life in the industrial north of England, and Homage to Catalonia (1938), an account of his experiences soldiering for the Republican faction of the Spanish Civil War (1936–1939), are 
    as critically respected as his essays on politics and literature, language and culture.
    Blair was born in India, and raised and educated in England. After school he became an Imperial policeman in Burma, before returning to Suffolk, England, where he began his writing 
    career as George Orwell—a name inspired by a favourite location, the River Orwell. He lived from occasional pieces of journalism, and also worked as a teacher or bookseller whilst 
    living in London. From the late 1920s to the early 1930s, his success as a writer grew and his first books were published. He was wounded fighting in the Spanish Civil War, leading to his first period of ill health on return to England. During the Second World War he worked as a journalist and for the BBC. 
    The publication of Animal Farm led to fame during his life-time. During the final years of his life he worked on 1984, and moved between Jura in Scotland and London.
    Orwell's work remains influential in popular culture and in political culture, and the adjective "Orwellian"—describing totalitarian and authoritarian social practices—is part of the 
    English language, like many of his neologisms, such as "Big Brother", "Thought Police", "Room 101", "Newspeak", "memory hole", "doublethink", and "thoughtcrime".
    In 2008, The Times ranked George Orwell second among "The 50 greatest British writers since 1945". Orwell was noted for very close and enduring friendships with a few friends, but these were generally people with a similar background or with a similar level of literary ability. Ungregarious, he was out of place in a crowd and his discomfort was exacerbated when he was outside his own class. Though representing himself as a spokesman for the common man, he often appeared out of place with real working people. His brother-in-law Humphrey Dakin, a "Hail fellow, well met" type, who took him to a local pub in Leeds, said that he was told by the landlord: "Don't bring that bugger in here again."[209] Adrian Fierz commented "He wasn't interested in racing or greyhounds or pub crawling or shove ha'penny. 
    He just did not have much in common with people who did not share his intellectual interests." 
    Awkwardness attended many of his encounters with working-class representatives, as with Pollitt and McNair,[211] but his courtesy and good manners were often commented on. Jack Common observed on meeting him for the first time, "Right away manners, and more than manners—breeding—showed through."[212]
    In his tramping days, he did domestic work for a time. His extreme politeness was recalled by a member of the family he worked for; she declared that the family referred to him as "Laurel" after the film comedian.[49] With his gangling figure and awkwardness, Orwell's friends often saw him as a figure of fun. Geoffrey Gorer commented "He was awfully likely to knock things off tables, trip over things. I mean, he was a gangling, physically badly co-ordinated young man. I think his feeling [was] that even the inanimate world was against him."[213] When he shared a flat with Heppenstall and Sayer, he was treated in a patronising manner by the younger men.[214] At the BBC in the 1940s, "everybody would pull his leg"[215] and Spender described him as having real entertainment value "like, as I say, watching a Charlie Chaplin movie".[216] A friend of Eileen's reminisced about her tolerance and humour, often at Orwell's expense.[202]
    One biography of Orwell accused him of having had an authoritarian streak.[217] In Burma, he struck out at a Burmese boy who, while "fooling around" with his friends, had "accidentally bumped into him" at a station, resulting in Orwell falling "heavily" down some stairs.[218] One of his former pupils recalled being beaten so hard he could not sit down for a week.[219] When sharing a flat with Orwell, Heppenstall came home late one night in an advanced stage of loud inebriation. The upshot was that Heppenstall ended up with a bloody nose and was locked in a room. When he complained, Orwell hit him across the legs with a shooting stick and Heppenstall then had to defend himself with a chair. Years later, after Orwell's death, Heppenstall wrote a dramatic account of the incident called "The Shooting Stick" and Mabel Fierz confirmed that Heppenstall came to her in a sorry state the following day.#
    Orwell got on well with young people. The pupil he beat considered him the best of teachers and the young recruits in Barcelona tried to drink him under the table without success. 
    His nephew recalled Uncle Eric laughing louder than anyone in the cinema at a Charlie Chaplin film.[201]
    In the wake of his most famous works, he attracted many uncritical hangers-on, but many others who sought him found him aloof and even dull. With his soft voice, he was sometimes 
    shouted down or excluded from discussions.[222] At this time, he was severely ill; it was wartime or the austerity period after it; during the war his wife suffered from depression; 
    and after her death he was lonely and unhappy. In addition to that, he always lived frugally and seemed unable to care for himself properly. As a result of all this, people found his 
    circumstances bleak.[223] Some, like Michael Ayrton, called him "Gloomy George", but others developed the idea that he was an "English secular saint".[224]
    Although Orwell was frequently heard on the BBC for panel discussion and one-man broadcasts, no recorded copy of his voice is known to exist.
