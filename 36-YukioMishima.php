<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Yukio_Mishima">Wikipedia</a>
    <br>
    Yukio Mishima[a] (Mishima Yukio, 14 January 1925 - 25 November 1970), born Kimitake Hiraoka (Hiraoka Kimitake), was a Japanese author, poet, playwright, actor, 
    model, Shintoist, nationalist, and founder of the Tatenokai ("Shield Society"), an unarmed civilian militia. Mishima is considered one of the most important Japanese authors of 
    the 20th century. He was considered for the Nobel Prize in Literature in 1968, but the award went to his countryman and benefactor Yasunari Kawabata.His works include the novels 
    Confessions of a Mask ( Kamen no kokuhaku) and The Temple of the Golden Pavilion (Kinkaku-ji), and the autobiographical essay Sun and Steel (Taiyō to tetsu). Mishima's work is characterized by "its 
luxurious vocabulary and decadent metaphors, its fusion of traditional Japanese and modern Western literary styles, and its obsessive assertions of the unity of beauty, eroticism and death",according to author Andrew Rankin.
    Mishima's political activities made him a controversial figure, which he remains in modern Japan.From his mid-30s, Mishima's right-wing ideology was revealed increasingly. 
    He was proud of the traditional culture and spirit of Japan, and opposed what he saw as western-style materialism, along with Japan's postwar democracy, globalism, and communism, 
    worrying that by embracing these ideas the Japanese people would lose their "national essence" (kokutai) and their distinctive cultural heritage (Shinto and Yamato-damashii) to become 
    a "rootless" people. Mishima formed the Tatenokai for the avowed purpose of restoring sacredness and dignity to the Emperor of Japan. On 25 November 1970, 
    Mishima and four members of his militia entered a military base in central Tokyo, took its commandant hostage, and tried to inspire the Japan Self-Defense Forces to rise up and 
    overthrow Japan's 1947 Constitution, which he called "a constitution of defeat".After his speech and screaming of "Long live the Emperor!", he committed seppuku.
    In August 1966, Mishima visited Ōmiwa Shrine in Nara Prefecture, thought to be one of the oldest Shintō shrines in Japan, as well as the hometown of his mentor Zenmei Hasuda and the areas associated with the Shinpūren Rebellion , an uprising against the Meiji government by samurai in 1876. This trip would become the inspiration for portions of Runaway Horses (奔馬, Honba), the second novel in the Sea of Fertility tetralogy. While in Kumamoto, Mishima purchased a Japanese sword for 100,000 yen. Mishima envisioned the reincarnation of Kiyoaki, the protagonist of the first novel Spring Snow, as a man named Isao who put his life on the line to bring about a restoration of direct rule by the Emperor against the backdrop of the League of Blood Incident (血盟団事件, Ketsumeidan jiken) in 1932.
    From 12 April to 27 May 1967, Mishima underwent basic training with the Ground Self-Defense Force (GSDF). Mishima had originally lobbied to train with the GSDF for six months, 
    but was met with resistance from the Defense Agency.[166] Mishima's training period was finalized to 46 days, which required using some of his connections. His participation in GSDF training was kept secret, both because 
the Defense Agency did not want to give the impression that anyone was receiving special treatment, and because Mishima wanted to experience "real" military life. As such, Mishima trained under his birth name, Kimitake Hiraoka, and most of his fellow soldiers did not recognize him.[166]
    From June 1967, Mishima became a leading figure in a plan to create a 10,000-man "Japan National Guard" as a civilian complement to Japan's Self Defense Forces. He began leading groups of right-wing college students to undergo 
basic training with the GDSF in the hope of training 100 officers to lead the National Guard.
    Like many other right-wingers, Mishima was especially alarmed by the riots and revolutionary actions undertaken by radical "New Left" university students, who took over dozens of college campuses in Japan in 1968 and 1969. 
On 26 February 1968, the 32nd anniversary of the February 26 Incident, he and several other right-wingers met at the editorial offices of the recently founded right-wing magazine Controversy Journal ( Ronsō jaanaru), where they 
pricked their little fingers and signed a blood oath promising to die if necessary to prevent a left-wing revolution from occurring in Japan.Mishima showed his sincerity by signing his birth name, Kimitake Hiraoka, in his own blood.
    When Mishima found that his plan for a large-scale Japan National Guard with broad public and private support failed to catch on,he formed the Tatenokai (楯の会, "Shield Society") on 5 October 1968, a private militia composed primarily of right-wing 
college students who swore to protect the Emperor of Japan. The activities of the Tatenokai primarily focused on martial training and physical fitness, including traditional kendo sword-fighting and long-distance running. Mishima personally oversaw this training himself. Initial membership was around 50, and was drawn primarily from students from Waseda University and individuals affiliated with Controversy Journal. The number of Tatenokai members later increased to 100. Some of the members had graduated from university and were employed, while some were already working adults when they enlisted.

    On 25 November 1970, Mishima and four members of the Masakatsu Morita Masahiro Ogawa Masayoshi Koga and Hiroyasu Koga used a pretext to visit the commandant Kanetoshi Mashita of Camp Ichigaya, a military base in central
 Tokyo and the headquarters of the Eastern Command of the Japan Self-Defense Forces. Inside, they barricaded the office and tied the commandant to his chair. Mishima wore a white hachimaki headband with a red hinomaru circle 
in the center bearing the kanji for "To be reborn seven times to serve the country" (七生報國, Shichishō hōkoku), which was a reference to the last words of Kusunoki Masasue, the younger brother of the 14th century imperial 
loyalist samurai Kusunoki Masashige (楠木正成), as the two brothers died fighting to defend the Emperor. With a prepared manifesto and a banner listing their demands, Mishima stepped out onto the balcony to address the 
soldiers gathered below. His speech was intended to inspire a coup d'état to restore the power of the emperor. 
He succeeded only in irritating the soldiers, and was heckled, with jeers and the noise of helicopters drowning out some parts of his speech. In his speech Mishima rebuked the JSDF for their passive acceptance of a constitution that "denies (their) own existence" and shouted to rouse them, "Where has the spirit of the samurai gone?" In his final written appeal that Morita and Ogawa scattered copies of from the balcony, Mishima expressed his dissatisfaction with the half-baked nature of the JSDF:
    It is self-evident that the United States would not be pleased with a true Japanese volunteer army protecting the land of Japan.
