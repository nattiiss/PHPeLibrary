<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/J._K._Rowling">Wikipedia</a>
    <br>
    Joanne Rowling CH OBE HonFRSE FRCPE FRSL (born 31 July 1965), also known by her pen name J. K. Rowling, is a British author and philanthropist. She wrote a seven-volume children's fantasy series, Harry Potter, published from 1997 to 2007. The series has been enormously successful: it has sold over 500 million copies, been translated into at least 70 languages, and spawned a global media franchise including films and video games. The Casual Vacancy (2012) was her first novel for adults. She writes Cormoran Strike, an ongoing crime fiction series, as Robert Galbraith.
    Born in Yate, Rowling graduated with a degree in French from the University of Exeter in 1987 and began working temp jobs as a bilingual secretary. In 1990, the idea for the characters of Harry Potter came to her while she waited on a delayed train; later that year, her mother died of multiple sclerosis. In the seven years before publication of the first Harry Potter novel, Harry Potter and the Philosopher's Stone (1997), Rowling moved to Portugal, married, had a daughter, relocated to Scotland when her marriage failed, divorced, and earned a teaching certificate. She wrote while living on state assistance as a single parent, deeply affected by her mother's death. By 2008, Forbes had named her the world's highest-paid author.
    Rowling concluded the Harry Potter series with Harry Potter and the Deathly Hallows (2007). The novels follow a boy named Harry Potter as he attends Hogwarts, a school for wizards, and battles Lord Voldemort. Death and the divide between good and evil are the central themes of the series. Its influences include the Bildungsroman (coming-of-age story), school stories, fairy tales, and Christian allegory. The series revived fantasy as a genre in the children's market, spawned a host of imitators, and inspired an active fandom. Critical reception has been more mixed. Many reviewers see Rowling's writing as conventional; some regard her portrayal of gender and social division as regressive. There were also religious debates over Harry Potter.
    Rowling has won many accolades for her work. She was named to the Order of the British Empire and was appointed a member of the Order of the Companions of Honour for services to literature and philanthropy. Harry Potter brought her wealth and recognition that she has used to advance philanthropic endeavours and political causes. She co-founded the charity Lumos and established the Volant Charitable Trust, named after her mother. Rowling's charitable giving centres on medical causes and supporting at-risk women and children. In politics, she has donated to Britain's Labour Party and opposed Scottish independence and Brexit. Since late 2019, she has publicly expressed her opinions on transgender people and related civil rights. 
    These have been criticised as transphobic by LGBT rights organisations and some feminists, but have received support from other feminists and individuals.
    Rowling completed Harry Potter and the Philosopher's Stone in June 1995.[116] The initial draft included an illustration of Harry by a fireplace, showing a lightning-shaped scar on his forehead.[117] Following an enthusiastic report from an early reader,[118] Christopher Little Literary Agency agreed to represent Rowling. Her manuscript was submitted to twelve publishers, all of which rejected it.[11] Barry Cunningham, who ran the children's literature department at Bloomsbury Publishing, eventually bought it.[119] Nigel Newton, who headed Bloomsbury at the time, decided to go 
    ahead with the manuscript after his eight-year-old daughter finished one chapter and wanted to keep reading.[38][120] Although Bloomsbury agreed to publish the book, one of Rowling's favourite memories was of Cunningham telling her, "You'll never make any money out of children's books, Jo."[121] Rowling was awarded a writer's grant by the Scottish Arts Council[l] to support her childcare costs and finances before Philosopher's Stone's publication, and to aid in writing the sequel, Chamber of Secrets.[122][123] On 26 June 1997, Bloomsbury published Philosopher's Stone with an initial print run of 500 copies.[124][125] Before Chamber of Secrets was published, Rowling had received only Â£2,800 ($4,200) in royalties.[126]
    Philosopher's Stone introduces Harry Potter. Harry is a wizard who lives with his non-magical relatives until his eleventh birthday, when he is invited to attend Hogwarts School of Witchcraft and Wizardry.[127][128] Rowling wrote six sequels, which follow Harry's adventures at Hogwarts with friends Hermione Granger and Ron Weasley and his attempts to defeat Lord Voldemort, who killed Harry's parents when he was a child.[127] In Philosopher's Stone, Harry foils Voldemort's plan to acquire an elixir of life; in Deathly Hallows, the final book, he kills Voldemort.[127]
    Rowling received the news that the US rights were being auctioned at the Bologna Children's Book Fair.[129] To her surprise and delight, Scholastic Corporation bought the rights for $105,000.[130] She bought an apartment in Edinburgh with the money from the sale.[131] Arthur A. Levine, head of the imprint at Scholastic, pushed for a name change. He wanted Harry Potter and the School of Magic; as a compromise Rowling suggested Harry Potter and the Sorcerer's Stone.[132] Sorcerer's Stone was released in the United States in September 1998.[133] It was not widely reviewed, but the reviews it received were generally positive.[134] Sorcerer's Stone became a New York Times bestseller by December.[135]
    The next three books in the series were released in quick succession between 1998 and 2000: Harry Potter and the Chamber of Secrets (1998), Harry Potter and the Prisoner of Azkaban (1999), and Harry Potter and the Goblet of Fire (2000), each selling millions of copies.[136] When Harry Potter and the Order of the Phoenix was delayed and had not appeared by 2002, rumours circulated that Rowling was suffering writer's block.[137] It was published in June 2003, selling millions of copies on the first day.[138] Two years later, Harry Potter and the Half-Blood Prince was released in July, again selling millions of copies on the first day.[139] The series ended with Harry Potter and the Deathly Hallows, published in July 2007.
