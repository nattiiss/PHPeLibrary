<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Douglas_Adams">Wikipedia</a>
    <br>
Douglas Noel Adams (11 March 1952 â€“ 11 May 2001) was an English author, screenwriter, essayist, humourist, satirist and dramatist. Adams was author of The Hitchhiker's 
Guide to the Galaxy, which originated in 1978 as a BBC radio comedy, before developing into a "trilogy" of five books that sold more than 15 million copies in his lifetime 
and generated a television series, several stage plays, comics, a video game, and a 2005 feature film. Adams's contribution to UK radio is commemorated in The Radio Academy's 
Hall of Fame. Adams also wrote Dirk Gently's Holistic Detective Agency (1987) and The Long Dark Tea-Time of the Soul (1988), and co-wrote The Meaning of Liff (1983), The Deeper 
Meaning of Liff (1990), and Last Chance to See (1990). He wrote two stories for the television series Doctor Who, co-wrote City of Death (1979), and served as script editor for 
its seventeenth season. He co-wrote the sketch "Patient Abuse" for the final episode of Monty Python's Flying Circus. A posthumous collection of his selected works, including 
the first publication of his final (unfinished) novel, was published as The Salmon of Doubt in 2002. Adams was an advocate for environmentalism and conservation, a lover of fast cars,
technological innovation and the Apple Macintosh, and a self-proclaimed "radical atheist".
After leaving university Adams moved back to London, determined to break into TV and radio as a writer. An edited version of the Footlights Revue appeared on BBC2 television in 1974. 
A version of the Revue performed live in London's West End led to Adams being discovered by Monty Python's Graham Chapman. The two formed a brief writing partnership, earning Adams a 
writing credit in episode 45 of Monty Python for a sketch called "Patient Abuse". The pair also co-wrote the "Marilyn Monroe" sketch which appeared on the 
soundtrack album of Monty Python and the Holy Grail. Adams is one of only two people other than the original Python members to get a writing credit (the other being Neil Innes).
The Hitchhiker's Guide to the Galaxy was a concept for a science-fiction comedy radio series pitched by Adams and radio producer Simon Brett to BBC Radio 4 in 1977. Adams came up with an outline 
for a pilot episode, as well as a few other stories (reprinted in Neil Gaiman's book Don't Panic: The Official Hitchhiker's Guide to the Galaxy Companion) that could be used in the series.
Towel Day 2005 in Innsbruck, Austria, where Adams first had the idea of The Hitchhiker's Guide. In the novels, a towel is the most useful thing a space traveller can have. The annual Towel Day (25 May) 
was first celebrated in 2001, two weeks after Adams's death. According to Adams, the idea for the title occurred to him while he lay drunk in a field in Innsbruck, Austria, gazing at the stars. He was 
carrying a copy of the Hitch-hiker's Guide to Europe, and it occurred to him that "somebody ought to write a Hitchhiker's Guide to the Galaxy".Despite the original outline, Adams was said to make up the stories as he wrote. 
He turned to John Lloyd for help with the final two episodes of the first series. Lloyd contributed bits from an unpublished science fiction book of his own, called GiGax. Very little of Lloyd's material survived in 
later adaptations of Hitchhiker's, such as the novels and the TV series. The TV series was based on the first six radio episodes, and sections contributed by Lloyd were largely re-written.
BBC Radio 4 broadcast the first radio series weekly in the UK starting 8 March 1978, lasting until April.The series was distributed in the United States by National Public Radio. Following the success of the first series, 
another episode was recorded and broadcast, which was commonly known as the Christmas Episode. A second series of five episodes was broadcast one per night, during the week of 25 January 1980.
While working on the radio series (and with simultaneous projects such as The Pirate Planet) Adams developed problems keeping to writing deadlines that got worse as he published novels. Adams was never a prolific writer 
and usually had to be forced by others to do any writing. This included being locked in a hotel suite with his editor for three weeks to ensure that So Long, and Thanks for All the Fish was completed.
He was quoted as saying, "I love deadlines. I love the whooshing noise they make as they go by." Despite the difficulty with deadlines, Adams wrote five novels in the series, published in 1979, 1980, 1982, 1984, and 1992.
The books formed the basis for other adaptations, such as three-part comic book adaptations for each of the first three books, an interactive text-adventure computer game, and a photo-illustrated edition, published in 1994. 
This latter edition featured a 42 Puzzle designed by Adams, which was later incorporated into paperback covers of the first four Hitchhiker's novels (the paperback for the fifth re-used the artwork from the hardback edition).
In 1980, Adams began attempts to turn the first Hitchhiker's novel into a film, making several trips to Los Angeles, and working with Hollywood studios and potential producers. The next year, the radio series became the basis for 
a BBC television mini-series[34] broadcast in six parts. When he died in 2001 in California, he had been trying again to get the film project started with Disney, which had bought the rights in 1998. The screenplay got a posthumous 
re-write by Karey Kirkpatrick, and the resulting film was released in 2005. Radio producer Dirk Maggs had consulted with Adams, first in 1993, and later in 1997 and 2000 about creating a third radio series, based on the third 
novel in the Hitchhiker's series. They also discussed the possibilities of radio adaptations of the final two novels in the five-book "trilogy". As with the film, this project was realised only after Adams's death. The third series, 
The Tertiary Phase, was broadcast on BBC Radio 4 in September 2004 and was subsequently released on audio CD. With the aid of a recording of his reading of Life, the Universe and Everything and editing, Adams can be heard playing 
the part of Agrajag posthumously. So Long, and Thanks for All the Fish and Mostly Harmless made up the fourth and fifth radio series, respectively (on radio they were titled The Quandary Phase and The Quintessential Phase) and these 
were broadcast in May and June 2005, and also subsequently released on Audio CD. The last episode in the last series (with a new, "more upbeat" ending) concluded with, "The very final episode of The Hitchhiker's Guide to the Galaxy by 
Douglas Adams is affectionately dedicated to its author."
