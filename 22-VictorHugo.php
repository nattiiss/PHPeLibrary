<h2>Biography</h2>
    <p>
    <br>
    <a href="https://de.wikipedia.org/wiki/Victor_Hugo">Wikipedia</a>
    <br>
    1827 schrieb Hugo sein erstes Stück, das Versdrama Cromwell. Dieses erwies sich zwar als kaum spielbar, das Vorwort jedoch, die berühmte Préface de Cromwell, wurde zum Manifest des neuen romantischen Theaters und überhaupt der romantischen Schule, deren unbestrittener Chef Hugo inzwischen war und die er in dem legendären Kreis des Cénacle um sich versammelte.
    1829 publizierte Hugo den Roman Le dernier jour dun condamné à mort, ein Plädoyer gegen die Todesstrafe und indirekte Regimekritik. Im selben Jahr verfasste er die melodramatischen historischen Stücke Marion Delorme, das vor der Aufführung als regimekritisch verboten wurde, und Hernani. Dessen Uraufführung am 25. Februar 1830 ging als bataille dHernani (Schlacht um Hernani) in die Literaturgeschichte ein, nämlich als lautstark im Publikum ausgetragene Auseinandersetzung zwischen den Anhängern des klassizistischen 
    Regeltheaters und den Adepten des neuen romantischen Theaters, das vor allem die „Wahrheit“ der Darstellung intendierte. Privat allerdings ging es Hugo weniger gut: Ehefrau Adèle begann ein Verhältnis mit seinem Freund und Literatenkollegen Charles-Augustin Sainte-Beuve, 
    das er hilflos duldete und dessen Reflexe sich in Gedichten der Sammlung Les feuilles dautomne („Herbstblätter“, Ende 1831) finden[2].
    1831 publizierte Hugo eines seiner erfolgreichsten Werke, den 1482 spielenden Roman Notre Dame de Paris, dessen zentrale Figuren und Handlungsstränge sich um die Kathedrale gruppieren und ein Bild vom spätmittelalterlichen Paris entwerfen sollen (weshalb der gängige deutsche Titel „Der Glöckner von Notre-Dame“ 
    nicht recht zutreffend ist). In den nächsten Jahren verfasste Hugo hauptsächlich historische Stücke, deren erstes, Le roi samuse („Der König amüsiert sich“, 1832), als politisch missliebig direkt nach der Premiere verboten wurde. Denn Hugo war, zusammen mit anderen jungen Intellektuellen, schon bald nach der Julirevolution von 1830 in Opposition zu dem neuen Regime von „Bürgerkönig“ Louis-Philippe I. gegangen. Die nächsten Stücke wurden jedoch zunehmend weniger kritisch. Es waren: 1833 Lucrèce Borgia und Marie Tudor, 1835 Angelo, 1838 Ruy Blas. 1837 lernte Hugo König Louis-Philippe persönlich kennen und näherte sich ihm politisch weiter an.
    Neben den belletristischen Arbeiten schrieb und publizierte Hugo auch ständig Gedichte, die er von Zeit zu Zeit gesammelt herausgab: Les Chants du crépuscule („Dämmerungsgesänge“, 1835), Les voix intérieures („Die inneren Stimmen“, 1837), Les rayons et les ombres („Strahlen und Schatten“, 1840).
    Zwischen 1838 und 1840 unternahm Hugo drei Rhein-Reisen, die ihn den ganzen Fluss entlang bis in die Schweiz führten. Seine detaillierten lokalen Beobachtungen und verallgemeinernden Schlussfolgerungen legte er in dem 1841 publizierten (dt. 1842) dreibändigen Werk „Der Rhein. Briefe an einen Freund“ dar. Im dritten Band formulierte Hugo Ideen zur deutsch-französischen Partnerschaft und zur Europäischen Einigung, die erst über hundert Jahre später Wirklichkeit wurden.
    Inzwischen ging es Hugo auch privat wieder gut: Anfang 1833 hatte er die vier Jahre jüngere Schauspielerin Juliette Drouet kennengelernt, mit der er bis zu ihrem Tod 1883 zusammenblieb. 1838 erwarb ein Verlag für enorme 300.000 Francs die Rechte an seinen bisherigen Werken. 1841 wurde er nach mehreren Anläufen endlich in die Académie française gewählt. 1843 allerdings war sein Drama Les Burgraves („Die Burggrafen“)[3] ein kompletter Misserfolg, der ihm für immer die Freude am Theater verdarb. Ein anderer, größerer Schicksalsschlag war im selben Jahr der Tod seiner jungverheirateten Lieblingstochter Léopoldine, die mit ihrem Mann bei einer Wattwanderung nahe Le Havre von der Flut überrascht wurde.
    Im Jahre 1845 erhob König Louis-Philippe Hugo zum Vicomte und Pair, das heißt zum Mitglied auf Lebenszeit der Pairskammer, des parlamentarischen Oberhauses (das allerdings nach der Februarrevolution 1848 abgeschafft wurde).
    Nachdem Hugo 1846 jedoch erneut seine politische Richtung geändert hatte und zum Republikaner mutiert war, begann Hugo 1847 einen sozial engagierten Roman in der Manier von Eugène Sues berühmten Les mystères de Paris, der aber erst 1862 als Les Misérables (dt. heute: Die Elenden, früher: Die Miserablen) fertig werden sollte. Beim Ausbruch der Februarrevolution 1848 war er zunächst begeistert, schlug sich nach dem Juni-Aufstand der Pariser Arbeiter aber auf die Seite des konservativen „Parti de lordre“ und dann auf die des neugewählten Präsidenten Louis-Napoléon Bonaparte. Zum konservativen Abgeordneten gewählt, verwirrte er jedoch seine politischen Freunde durch sozial engagierte und politisch liberale Reden.
