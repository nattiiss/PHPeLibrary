<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Arthur_Conan_Doyle">Wikipedia</a>
    <br>
    Sir Arthur Ignatius Conan Doyle KStJ DL (22 May 1859 – 7 July 1930) was a British writer and physician. He created the character Sherlock Holmes in 1887 for A Study in Scarlet, the first 
    of four novels and fifty-six short stories about Holmes and Dr. Watson. The Sherlock Holmes stories are milestones in the field of crime fiction.
Doyle was a prolific writer; other than Holmes stories, his works include fantasy and science fiction stories about Professor Challenger and humorous stories about the Napoleonic soldier Brigadier Gerard, 
as well as plays, romances, poetry, non-fiction, and historical novels. One of Doyle's early short stories, "J. Habakuk Jephson's Statement" 
(1884), helped to popularise the mystery of the Mary Celeste.
Doyle struggled to find a publisher. His first work featuring Sherlock Holmes and Dr. Watson, A Study in Scarlet, was written in three weeks when he was 27 and was accepted for 
publication by Ward Lock & Co on 20 November 1886, which gave Doyle £25 (equivalent to £2,900 in 2019) in exchange for all rights to the story. The piece appeared a year later in the Beeton's Christmas 
Annual and received good reviews in The Scotsman and the Glasgow Herald.
Holmes was partially modelled on Doyle's former university teacher Joseph Bell. In 1892, in a letter to Bell, Doyle wrote, "It is most certainly to you that I owe Sherlock Holmes ... round the centre of
 deduction and inference and observation which I have heard you inculcate I have tried to build up a man", and in his 1924 autobiography, he remarked, "It is no wonder that after the study of such a character 
 [viz., Bell] I used and amplified his methods when in later life I tried to build up a scientific detective who solved cases on his own merits and not through the folly of the criminal."Robert Louis Stevenson was 
 able to recognise the strong similarity between Joseph Bell and Sherlock Holmes: "My compliments on your very ingenious and very interesting adventures of Sherlock Holmes. ... can this be my old friend Joe Bell?"
  Other authors sometimes suggest additional influences—for instance, Edgar Allan Poe's character C. Auguste Dupin, who is mentioned, disparagingly, by Holmes in A Study in Scarlet. Dr. (John) Watson owes his
   surname, but not any other obvious 
characteristic, to a Portsmouth medical colleague of Doyle's, Dr. James Watson.
Another of Doyle's longstanding interests was architectural design. In 1895, when he commissioned an architect friend of his, Joseph Henry Ball, to build him a home, he played an active part in the design process.
The home in which he lived from October 1897 to September 1907, known as Undershaw (near Hindhead, in Surrey), was used as a hotel and restaurant from 1924 until 2004, when it was bought by a developer and then stood empty 
while conservationists and Doyle fans fought to preserve it. In 2012, the High Court in London ruled in favor of those seeking to preserve the historic building, ordering that the redevelopment permission be quashed on the ground 
that it had not been obtained through proper procedures. The building was later approved to become part of Stepping Stones, a school for children with disabilities and special needs.
Doyle made his most ambitious foray into architecture in March 1912, while he was staying at the Lyndhurst Grand Hotel: He sketched the original designs for a third-storey extension and for an alteration of the front facade of the 
building. Work began later that year, and when it was finished, the building was a nearly exact manifestation of the plans Doyle had sketched. Superficial alterations have been subsequently made, but the essential structure is 
still clearly Doyle's.
In 1914, on a family trip to the Jasper National Park in Canada, he designed a golf course and ancillary buildings for a hotel. The plans were realised in full, but neither the golf course nor the buildings have survived.
