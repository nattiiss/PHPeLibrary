<h2>Biography</h2>
    <p>
    <br>
    <a href="https://de.wikipedia.org/wiki/Louisa_May_Alcott">Wikipedia</a>
    <br>
    Louisa May Alcott (November 29, 1832 - March 6, 1888) was an American novelist, short story writer, and poet best known
    as the author of the novel Little Women (1868) and its sequels Little Men (1871) and Jo's Boys (1886). Raised in New England 
    by her transcendentalist parents, Abigail May and Amos Bronson Alcott, she grew up among many well-known intellectuals of 
    the day, such as Ralph Waldo Emerson, Nathaniel Hawthorne, Henry David Thoreau, and Henry Wadsworth Longfellow.
    Alcott frequently wrote in her journals about going on long walks and runs. She challenged prevailing social norms 
    regarding gender by encouraging her young female readers to run as well.
    During her girlhood and early womanhood, Alcott shared in her family's poverty and Transcendentalist ideals. In 1840, after several setbacks 
    with the school, her family moved to a cottage on two acres along the Concord River in Concord, Massachusetts. They moved to the Utopian Fruitlands community for a 
    brief interval in 1843-1844, and then after its collapse to rented rooms and subsequently, a house 
    in Concord purchased with her mother's inheritance and help from Emerson. Alcott's early education had included lessons from the naturalist Henry David Thoreau but had chiefly 
    been in the hands of her father. She later described these early years in a newspaper sketch entitled "Silver Pitchers" (1876) which relates the experiences of her family during 
    their experiment in "plain living and high thinking" at Fruitlands.
    As she grew older, she developed as both an abolitionist and a feminist. In 1847 the family housed a fugitive slave for one week, and in 1848 Alcott read and admired the "Declaration 
    of Sentiments" published by the Seneca Falls Convention on women's rights. Due to the family's poverty, she began Work at an early age as an occasional teacher, seamstress, governess, 
    domestic help, and writer. Her first book was Flower Fables (1854), tales originally written for Ellen, the daughter of Ralph Waldo Emerson.
    In 1860, Alcott began writing for the Atlantic Monthly, and she was a nurse in the Union Hospital at Georgetown, D.C., for six weeks in 1862-1863. Her letters home, revised and published 
    in the Commonwealth and collected as Moods (1864), were also considered promising content for publication.
    A lesser-known part of her work is the passionate, fiery novels and stories she wrote, usually under the pseudonym A. M. Barnard. These works include protagonists who are willful and 
    relentless in the pursuit of their own aims, which often include revenge on those who have humiliated or thwarted them. These works achieved immediate commercial success and remain highly 
    readable today.
    In contrast, Alcott also produced moralistic and wholesome stories for children, and with the exceptions of the semi-autobiographical tale A Modern Mephistopheles (1877) which attracted 
    suspicion that it was authored by Julian Hawthorne, she did not return to creating works for adults.
    Louisa May Alcott's overwhelming success dated from the appearance of the first part of Little Women, remaining popular with her large and loyal public.
    Although the Jo character in Little Women was based on Louisa May Alcott, Alcott, unlike Jo, never married. In 1879 her younger sister May died, and Louisa May took in May's daughter, Louisa May Nieriker ("Lulu"), who was two years old.
    In her later life, Alcott became an advocate of women's suffrage and was the first woman to register to vote in Concord, Massachusetts.
