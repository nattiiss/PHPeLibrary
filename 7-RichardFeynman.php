<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Richard_Feynman">Wikipedia</a>
    <br>
    Richard Phillips Feynman (May 11, 1918 – February 15, 1988) was an American theoretical physicist, known for his work in the path integral formulation of quantum mechanics, the theory of quantum electrodynamics, the physics of the superfluidity of supercooled liquid helium, as well as his work in particle physics for which he proposed the parton model. For contributions to the development of 
    quantum electrodynamics, Feynman received the Nobel Prize in Physics in 1965 jointly with Julian Schwinger and Shin'ichirō Tomonaga. Feynman developed a widely used pictorial 
    representation scheme for the mathematical expressions describing the behavior of subatomic particles, which later became known as Feynman diagrams. During his lifetime, 
    Feynman became one of the best-known scientists in the world. In a 1999 poll of 130 leading physicists worldwide by the British journal Physics World, he was ranked the seventh-greatest physicist of all time.[2]
    He assisted in the development of the atomic bomb during World War II and became known to a wide public in the 1980s as a member of the Rogers Commission, the panel that investigated the Space Shuttle Challenger disaster. Along with his work in theoretical physics, Feynman has been credited with pioneering the field of quantum computing and introducing the concept of nanotechnology. He held the Richard C. Tolman professorship in theoretical physics at the California Institute of Technology.
    Feynman was a keen popularizer of physics through both books and lectures, including a 1959 talk on top-down nanotechnology called There's Plenty of Room at the Bottom and the 
    three-volume publication of his undergraduate lectures, The Feynman Lectures on Physics. Feynman also became known through his autobiographical books Surely You're Joking, 
    Mr. Feynman! and What Do You Care What Other People Think?, and books written about him such as Tuva or Bust! by Ralph Leighton and the biography Genius: The Life and Science of 
    Richard Feynman by James Gleick. In 1941, with World War II raging in Europe but the United States not yet at war, Feynman spent the summer working on ballistics problems at the Frankford Arsenal in Pennsylvania.[48][49] After the attack on Pearl Harbor brought the United States into the war, Feynman was recruited by Robert R. Wilson, who was working on means to produce enriched uranium for use in an atomic bomb, as part of what would become the Manhattan Project.[50][51] At the time, Feynman had not earned a graduate degree.[52] Wilson's team at Princeton was working on a device called an isotron, intended to electromagnetically separate uranium-235 from uranium-238. This was done in a quite different manner from that used 
    by the calutron that was under development by a team under Wilson's former mentor, Ernest O. Lawrence, at the Radiation Laboratory of the University of California. On paper, the isotron was many times more efficient than the calutron, but Feynman and Paul Olum struggled to determine whether or not it was practical. Ultimately, on Lawrence's recommendation, the isotron project was abandoned.[53]
    At this juncture, in early 1943, Robert Oppenheimer was establishing the Los Alamos Laboratory, a secret laboratory on a mesa in New Mexico where atomic bombs would be designed and 
    built. An offer was made to the Princeton team to be redeployed there. "Like a bunch of professional soldiers," Wilson later recalled, "we signed up, en masse, to go to Los Alamos."
     Like many other young physicists, Feynman soon fell under the spell of the charismatic Oppenheimer, who telephoned Feynman long distance from Chicago to inform him that he had found a Presbyterian sanatorium in Albuquerque, New Mexico for Arline. They were among the first to depart for New Mexico, leaving on a train on March 28, 1943. The railroad supplied Arline with a wheelchair, and Feynman paid extra for a private room for her. There they spent their wedding anniversary.[55]
    At Los Alamos, Feynman was assigned to Hans Bethe's Theoretical (T) Division, and impressed Bethe enough to be made a group leader. He and Bethe developed the Bethe–Feynman formula for 
    calculating the yield of a fission bomb, which built upon previous work by Robert Serber.[58] As a junior physicist, he was not central to the project. He administered the computation 
    group of human computers in the theoretical division. With Stanley Frankel and Nicholas Metropolis, he assisted in establishing a system for using IBM punched cards for computation.
     He invented a new method of computing logarithms that he later used on the Connection Machine.[60][61] Other work at Los Alamos included calculating neutron equations for the 
     Los Alamos "Water Boiler", a small nuclear reactor, to measure how close an assembly of fissile material was to criticality.
