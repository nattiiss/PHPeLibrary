<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/Jane_Austen">Wikipedia</a>
    <br>
    Jane Austen (16 December 1775 – 18 July 1817) was an English novelist known primarily for her six major novels, which interpret, critique, and comment upon the British landed gentry at the end of the 18th century. Austen's plots often explore the dependence of women on marriage in the pursuit of favourable social standing and economic security. Her works critique the novels of sensibility of the second half of the 18th century and are part of the transition to 19th-century literary realism.[2][b] Her use of biting irony, along with her realism and social commentary, have earned her acclaim among critics and scholars.
    With the publication of Sense and Sensibility (1811), Pride and Prejudice (1813), Mansfield Park (1814), and Emma (1816), she achieved modest success and little fame in her lifetime, as the books were published anonymously. She wrote two other novels—Northanger Abbey and Persuasion, both published posthumously in 1818—and began another, eventually titled Sanditon, but died before its completion. She also left behind: three volumes of juvenile writings in manuscript; the short epistolary novel Lady Susan; and another unfinished novel, The Watsons.
    Austen gained far more status after her death, and her six full-length novels have rarely been out of print. A significant transition in her posthumous reputation occurred in 1833, when her novels were republished in Richard Bentley's Standard Novels series, illustrated by Ferdinand Pickering, and sold as a set. They gradually gained wider acclaim and popular readership. In 1869, fifty-two years after her death, her nephew's publication of A Memoir of Jane Austen introduced a compelling version of her writing career and supposedly uneventful life to an eager audience.
    Austen has inspired a large number of critical essays and literary anthologies. Her novels have inspired many films, from 1940's Pride and Prejudice to more recent productions like Sense and Sensibility (1995) and Love & Friendship (2016).
    Like many women authors at the time, Austen published her books anonymously.[96] At the time, the ideal roles for a woman were as wife and mother, and writing for women was regarded at best as a secondary form of activity; a woman who wished to be a full-time writer was felt to be degrading her femininity, so books by women were usually published anonymously in order to maintain the conceit that the female writer was only publishing as a sort of part-time job, and was not seeking to become a "literary lioness" (i.e. a celebrity).[97]
    During her time at Chawton, Austen published four generally well-received novels. Through her brother Henry, the publisher Thomas Egerton agreed to publish Sense and Sensibility, which, like all of Austen's novels except Pride and Prejudice, was published "on commission", that is, at the author's financial risk. When publishing on commission, publishers would advance the costs of publication, repay themselves as books were sold and then charge a 10% commission for each book sold, paying the rest to the author. If a novel did not recover its costs through sales, the author was responsible for them.[98] The alternative to selling via commission was by selling the copyright, where an author received a one-time payment from the publisher for the manuscript, which occurred with Pride and Prejudice.[99] Austen's experience with Susan (the manuscript that became Northanger Abbey) where she sold the copyright to the publisher Crosby & Sons for £10, who did not publish the book, forcing her to buy back the copyright in order to get her work published, left Austen leery of this method of publishing.
    The final alternative, of selling by subscription, where a group of people would agree to buy a book in advance, was not an option for Austen as only authors who were well known or had an influential aristocratic patron who would recommend an up-coming book to their friends, could sell by subscription.[99] Sense and Sensibility appeared in October 1811, and was described as being written "By a Lady".[96] As it was sold on commission, Egerton used expensive paper and set the price at 15 shillings.[96]
    Reviews were favourable and the novel became fashionable among young aristocratic opinion-makers;[100] the edition sold out by mid-1813. Austen's novels were published in larger editions than was normal for this period. The small size of the novel-reading public and the large costs associated with hand production (particularly the cost of handmade paper) meant that most novels were published in editions of 500 copies or less to reduce the risks to the publisher and the novelist. Even some of the most successful titles during this period were issued in editions of not more than 750 or 800 copies and later reprinted if demand continued. Austen's novels were published in larger editions, ranging from about 750 copies of Sense and Sensibility to about 2,000 copies of Emma. It is not clear whether the decision to print more copies than usual of Austen's novels was driven by the publishers or the author. Since all but one of Austen's books were originally published "on commission", the risks of overproduction were largely hers (or Cassandra's after her death) and publishers may have been more willing to produce larger editions than was normal practice when their own funds were at risk. Editions of popular works of non-fiction were often much larger.[101]
