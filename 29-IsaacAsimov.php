    Isaac Asimov (1920 â€“ April 6, 1992) was an American 
    writer and professor of biochemistry at Boston University. During his lifetime, Asimov was considered one of the "Big Three" science fiction writers, along with Robert A. Heinlein and Arthur C. Clarke.[2] A prolific writer, he wrote or edited more than 500 books. He also wrote an estimated 90,000 letters and postcards.[c] Best known for his hard science fiction, Asimov also wrote mysteries and fantasy, as well as much nonfiction.
    Asimov's most famous work is the Foundation series,[3] the first three books of which won the one-time Hugo Award for "Best All-Time Series" in 1966.[4] His other major series are the 
    Galactic Empire series and the Robot series. The Galactic Empire novels are set in the much earlier history of the same fictional universe as the Foundation series. Later, with Foundation and Earth (1986), he linked this distant future to the Robot stories, creating a unified "future history" for his stories.[5] He also wrote over 380 short stories, including the social science fiction novelette "Nightfall," which in 1964 was voted the best short science fiction story of all time by the Science Fiction Writers of America. Asimov wrote the Lucky Starr series of juvenile science-fiction novels using the pen name Paul French.[6]
    Most of his popular science books explain concepts in a historical way, going as far back as possible to a time when the science in question was at its simplest stage. Examples include 
    Guide to Science, the three-volume Understanding Physics, and Asimov's Chronology of Science and Discovery. He wrote on numerous other scientific and non-scientific topics, such as chemistry, astronomy, mathematics, history, biblical exegesis, and literary criticism.
    He was president of the American Humanist Association.[7] Several entities have been named in his honor, including the asteroid (5020) Asimov,[8] a crater on the planet Mars,[9][10] a 
    Brooklyn elementary school,[11] Honda's humanoid robot, ASIMO,[12] and four literary awards.
    Asimov's career can be divided into several periods. His early career, dominated by science fiction, began with short stories in 1939 and novels in 1950. This lasted until about 1958, all but ending after publication of The Naked Sun (1957). He began publishing nonfiction as co-author of a college-level textbook called Biochemistry and Human Metabolism. Following the brief orbit of the first man-made satellite Sputnik I by the USSR in 1957, his production of nonfiction, particularly popular science books, greatly increased, with a consequent drop in his science fiction output. Over the next quarter century, he wrote only four science fiction novels, while writing over 120 nonfiction books.
    Starting in 1982, the second half of his science fiction career began with the publication of Foundation's Edge. From then until his death, Asimov published several more sequels and prequels to his existing novels, tying them together in a way he had not originally anticipated, making a unified series. There are, however, many inconsistencies in this unification, especially in his earlier stories.[108] Doubleday and Houghton Mifflin published about 60% of his work as of 1969, Asimov stating that "both represent a father image".[58]
    Asimov believed his most enduring contributions would be his "Three Laws of Robotics" and the Foundation series.[109] Furthermore, the Oxford English Dictionary credits his science fiction for introducing into the English language the words "robotics", "positronic" (an entirely fictional technology), and "psychohistory" (which is also used for a different study on historical motivations). Asimov coined the term "robotics" without suspecting that it might be an original word; at the time, he believed it was simply the natural analogue of words such as mechanics and hydraulics, but for robots. Unlike his word "psychohistory", the word "robotics" continues in mainstream technical use with Asimov's original definition. 
    Star Trek: The Next Generation featured androids with "positronic brains" and the first-season episode "Datalore" called the positronic brain "Asimov's dream".[110]
    Asimov was so prolific and diverse in his writing that his books span all major categories of the Dewey Decimal Classification except for category 100, philosophy and psychology.[111] Although Asimov did write several essays about psychology,[112] and forewords for the books The Humanist Way (1988) and In Pursuit of Truth (1982),[113] which were classified in the 100s category, none of his own books were classified in that category.[111]
    According to UNESCO's Index Translationum database, Asimov is the world's 24th-most-translated author.
