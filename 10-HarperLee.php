    Nelle Harper Lee (April 28, 1926 – February 19, 2016) was an American novelist best known for her 1960 novel To Kill a Mockingbird. It won the 1961 Pulitzer Prize 
    and has become a classic of modern American literature. Lee has received numerous accolades and honorary degrees, including the Presidential Medal of Freedom in 2007 which 
    was awarded for her contribution to literature.She assisted her close friend 
    Truman Capote in his research for the book In Cold Blood (1966).[4] Capote was the basis for the character Dill Harris in To Kill a Mockingbird.The plot and characters of To 
    Kill a Mockingbird are loosely based on Lee's observations of her family and neighbors, as well as an event that occurred near her hometown in 1936 when she was 10. 
    The novel deals with the irrationality of adult attitudes towards race and class in the Deep South of the 1930s, as depicted through the eyes of two children. It was inspired by
     racist attitudes in her hometown of Monroeville, Alabama. Go Set a Watchman, written in the mid 1950s, was published in July 2015 as a sequel to Mockingbird but was later confirmed 
     to be an earlier draft of Mockingbird. I never expected any sort of success with Mockingbird. I was hoping for a quick and merciful death at the hands of the reviewers, but at the 
     same time I sort of hoped someone would like it enough to give me encouragement. Public encouragement. I hoped for a little, as I said, but I got rather a whole lot, 
     and in some ways this was just about as frightening as the quick, merciful death I'd expected.
    — Harper Lee, quoted in Newquist, 1964. In 1949, Lee moved to New York City and took jobs — first at a bookstore, then as an airline reservation agent — while writing 
    in her spare time.[24] After publishing several long stories, Lee found an agent in November 1956; Maurice Crain would become a friend until his death decades later. The following
     month, at Michael Brown's East 50th Street townhouse, friends gave Lee a gift of a year's wages with a note: "You have one year off from your job to write whatever you please. 
     Merry Christmas." Like Lee, the tomboy Scout in the novel is the daughter of a respected small-town Alabama attorney. Scout's friend, Dill, was inspired by Lee's childhood friend 
     and neighbor, Truman Capote;[15] Lee, in turn, is the model for a character in Capote's first novel, Other Voices, Other Rooms, published in 1948. Although the plot of Lee's novel 
     involves an unsuccessful legal defense similar to one undertaken by her attorney father, 
     the 1931 landmark Scottsboro Boys interracial rape case may also have helped to shape Lee's social conscience.
    While Lee herself downplayed autobiographical parallels in the book, Truman Capote, mentioning the character Boo Radley in To Kill a Mockingbird, described 
    details he considered autobiographical: "In my original version of Other Voices, Other Rooms I had that same man living in the house that used to leave things 
    in the trees, and then I took that out. He was a real man, and he lived just down the road from us. We used to go and get those things out of the trees. Everything she wrote about 
    it is absolutely true. But you see, I take the same thing and transfer it into some Gothic dream, done in an entirely different way."
