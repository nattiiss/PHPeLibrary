<h2>Biography</h2>
    <p>
    <br>
    <a href=" https://en.wikipedia.org/wiki/Haruki_Murakami">Wikipedia</a>
    <br>
    Haruki Murakami (村上 春樹, Murakami Haruki, born January 12, 1949[1]) is a Japanese writer. His novels, essays, and short stories have been bestsellers in Japan as well as internationally, with his work translated into 50 languages[2] and have sold millions of copies outside 
    Japan.He has received numerous awards for his work, including the Gunzou Prize for
    New Writers, the World Fantasy Award, the Frank O'Connor International Short Story Award, the Franz Kafka Prize, and the Jerusalem Prize. Growing up in Kobe before moving to Tokyo
    to attend Waseda University, he published his first novel Hear the Wind Sing (1979) after working as the owner of a small jazz bar for seven years.[8] His notable works include the novels Norwegian Wood (1987), The Wind-Up Bird Chronicle (1994–95), Kafka on the Shore (2002), 
    and 1Q84 (2009–10), with 1Q84 ranked as the best work of Japan's Heisei era (1989-2019) by the national newspaper Asahi Shimbun's survey of literary experts.[9] His work spans genres including science fiction, fantasy, and crime fiction, and has become known for its use of magical realist elements.[10][11] His official website lists Raymond Chandler, Kurt Vonnegut, and Richard Brautigan as key inspirations to his work, while Murakami himself has cited Kazuo Ishiguro, Cormac McCarthy, and Dag Solstad as his favourite currently active writers.[8][12] Murakami has also published five short story collections, including his most recently published work, First Person Singular (2020), and non-fiction works including Underground (1997), inspired by personal interviews Murakami conducted with victims of the Tokyo subway sarin attack, and What I Talk About When I Talk About Running (2007), a series of personal essays about his experience as a marathon runner. 
    His fiction has polarized literary critics and the reading public. He has sometimes been criticised by Japan's literary establishment as un-Japanese, leading to Murakami's recalling 
    that he was a "black sheep in the Japanese literary world". Meanwhile, Murakami has been described by Gary Fisketjon, the editor of Murakami's collection The Elephant Vanishes (1993),
    as a "truly extraordinary writer", while Steven Poole of The Guardian praised Murakami as "among the world's greatest living novelists" for his oeuvre and achievements.
    Murakami was born in Kyoto, Japan, during the post-World War II baby boom and raised in Nishinomiya, Ashiya and Kobe.[19][20] He is an only child. His father was the son of a Buddhist 
    priest,[21] and his mother is the daughter of an Osaka merchant.[22] Both taught Japanese literature. His father was involved in the Second Sino-Japanese War, and was deeply traumatized by it, which would, in turn, affect Murakami.
    Since childhood, Murakami, like Kōbō Abe, has been heavily influenced by Western culture, particularly Western as well as Russian music and literature. He grew up reading a wide range 
    of works by European and American writers, such as Franz Kafka, Gustave Flaubert, Charles Dickens, Kurt Vonnegut, Fyodor Dostoyevsky, Richard Brautigan and Jack Kerouac.
    These Western influences distinguish Murakami from the majority of other Japanese writers.
    Murakami studied drama at Waseda University in Tokyo, where he met Yoko, now his wife. His first job was at a record store. Shortly before finishing his studies, Murakami opened a 
    coffee house and jazz bar, Peter Cat, in Kokubunji, Tokyo, which he ran with his wife, from 1974 to 1981.[28] The couple decided not to have children.
    Murakami is an experienced marathon runner and triathlon enthusiast, though he did not start running until he was 33 years old, after he began as a way to stay healthy despite the hours spent at his desk writing. On June 23, 1996, he completed his first ultramarathon, a 100 km race around Lake Saroma in Hokkaido, Japan.
    He discusses his relationship with running in his 2008 memoir What I Talk About When I Talk About Running.
