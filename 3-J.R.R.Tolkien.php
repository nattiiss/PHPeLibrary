<h2>Biography</h2>
    <p>
    <br>
    <a href="https://en.wikipedia.org/wiki/J._R._R._Tolkien">Wikipedia</a>
    <br>
    John Ronald Reuel Tolkien CBE FRSL (3 January 1892 - 2 September 1973) was an English writer, poet, philologist, and academic, best known as the author of the high 
    fantasy works The Hobbit and The Lord of the Rings. From 1925 to 1945, Tolkien was the Rawlinson and Bosworth Professor of Anglo-Saxon and a Fellow of Pembroke College, 
    both at the University of Oxford. He then moved within the same university, to become the Merton Professor of English Language and Literature and Fellow of Merton College, 
    positions he held from 1945 until his retirement in 1959. Tolkien was a close friend of C. S. Lewis, a co-member of the informal literary discussion group The Inklings. 
    He was appointed a Commander of the Order of the British Empire by Queen Elizabeth II on 28 March 1972. After Tolkien's death, his son Christopher published a series of works 
    based on his father's extensive notes and unpublished manuscripts, including The Silmarillion. These, together with The Hobbit and The Lord of the Rings, form a connected body of tales, 
    poems, fictional histories, invented languages, and literary essays about a fantasy world called Arda and, within it, Middle-earth. Between 1951 and 1955, Tolkien applied the term 
    legendarium to the larger part of these writings. While many other authors had published works of fantasy before Tolkien, the great success of The Hobbit and The Lord of the Rings led 
    directly to a popular resurgence of the genre. This has caused Tolkien to be popularly identified as the "father" of modern fantasy literatureâ€”or, more precisely, of high fantasy.
    Tolkien's fantasy books on Middle-earth, especially The Lord of the Rings and The Silmarillion, drew on a wide array of influences including his philological interest in language,
    Christianity, mythology, archaeology, ancient and modern literature, and personal experience. His philological work centred on the study of Old English literature, especially
     Beowulf, and he acknowledged its importance to his writings. He was a gifted linguist, influenced by Germanic,Celtic,Finnish,and Greek language and 
     mythology. Commentators have attempted to identify many literary and topological antecedents for characters, places and events in Tolkien's writings. Some writers were important to him, 
     including the Arts and Crafts polymath William Morris, and he undoubtedly made use of some real place-names, such as Bag End, the name of his aunt's home. He acknowledged, too, 
     John Buchan and H. Rider Haggard, authors of modern adventure stories that he enjoyed. The effects of some specific experiences have been identified. Tolkien's childhood in the English 
     countryside, and its urbanization by the growth of Birmingham, influenced his creation of the Shire, while his personal experience of fighting in the trenches of the First World War affected 
     his depiction of Mordor.
     Parallel to Tolkien's professional work as a philologist, and sometimes overshadowing this work, to the effect that his academic output remained rather thin, was his affection for constructing languages. 
     The most developed of these are Quenya and Sindarin, the etymological connection between which formed the core of much of Tolkien's legendarium. Language and grammar for 
     Tolkien was a matter of aesthetics and euphony, and Quenya in particular was designed from "phonaesthetic" considerations; it was intended as an "Elven-latin", and was phonologically based on Latin, with ingredients from 
    Finnish, Welsh, English, and Greek. A notable addition came in late 1945 with Adunaic or Numenorean, a language of a "faintly Semitic flavour", connected with Tolkien's Atlantis legend, which by The Notion Club Papers ties 
    directly into his ideas about the inability of language to be inherited, and via the "Second Age" and the story of Earendil was grounded in the legendarium, thereby providing a link of Tolkien's 20th-century "real primary world" with the legendary past of his Middle-earth.
    Tolkien considered languages inseparable from the mythology associated with them, and he consequently took a dim view of auxiliary languages: in 1930 a congress of Esperantists were told as much by him, in his lecture A Secret 
    Vice, "Your language construction will breed a mythology", but by 1956 he had concluded that "Volapuk, Esperanto, Ido, Novial, &c, &c, are dead, far deader than ancient unused languages, because their authors never invented any 
    Esperanto legends".
    The popularity of Tolkien's books has had a small but lasting effect on the use of language in fantasy literature in particular, and even on mainstream dictionaries, which today commonly accept Tolkien's idiosyncratic spellings 
    dwarves and dwarvish (alongside dwarfs and dwarfish), which had been little used since the mid-19th century and earlier. (In fact, according to Tolkien, had the Old English plural survived, it would have been dwarrows or dwerrows.) 
    He also coined the term eucatastrophe, though it remains mainly used in connection with his own work.
